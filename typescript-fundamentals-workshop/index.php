        <h2 id="day2">Day 2: Intermediate TypeScript</h2>

        <p>In this workshop, we will explore some of the more advanced features of TypeScript, including unions, intersections, type aliases, generic types, and relative types. We will also cover control flow analysis, predefined mapped and conditional types, and generic functions.</p>
        <p>By the end of the workshop, you will have a deeper understanding of these TypeScript concepts and be able to apply them to your own projects to improve code quality, readability, and maintainability.</p>

        <br />
        <h3>Who is this workshop for?</h3>
        <p>Participants should have a <strong>basic understanding of TypeScript</strong> and be familiar with type annotations, primitive types, object types, function types, and class types. Participants should also have extensive experience working with JavaScript.</p>

        <br />
        <h3>Learning Objectives</h3>
        <ul>
          <li>Understand the differences between <strong>unions and intersections</strong> to model complex data types. </li>
          <li>Learn how to create <strong>type aliases</strong> to simplify complex type definitions. </li>
          <li>Gain familiarity with <strong>control flow analysis</strong> in TypeScript. </li>
          <li>Explore the power and flexibility of <strong>generic types</strong> to write reusable, type-safe code. </li>
          <li>Learn how to use <strong>relative types</strong>, such as keyof, typeof, and indexed types. </li>
          <li>Understand predefined <strong>mapped and conditional types</strong>. </li>
          <li>Learn how to write <strong>generic functions</strong> in TypeScript. </li>
        </ul>

